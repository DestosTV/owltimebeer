  <h2>À propos…</h2>
  <p>
    Owl Time est bien plus qu’une simple bière. C’est une stout anglaise artisanale, née au cœur de l’Angleterre, où tradition et créativité se rencontrent. Brassée avec passion, elle se distingue par ses arômes profonds de café fraîchement moulu et une délicate touche de vanille, qui lui confèrent une rondeur en bouche unique. 
    <br><br>
    Pensée pour les amateurs de saveurs raffinées, Owl Time incarne l’élégance britannique avec un soupçon d’audace. Son identité visuelle, marquée par un hibou stylisé, reflète à la fois la sagesse, l’optimisme et l’éclat que nous voulons transmettre dans chaque gorgée. C’est une bière qui se déguste lentement, avec intention, dans un moment de partage ou de réflexion.
